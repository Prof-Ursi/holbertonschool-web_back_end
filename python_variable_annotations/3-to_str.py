#!/usr/bin/env python3
""" Module with a string conversion function. """


def to_str(n: float) -> str:
    """
    Description:
        Converts a float to its string representation.

    Args:
        n (float): the floating-point number to convert.

    Returns:
        (str): the string conversion of the input float `n`.
    """
    return (str(n))
