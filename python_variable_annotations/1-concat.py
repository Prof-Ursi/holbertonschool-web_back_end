#!/usr/bin/env python3
""" Module with a string concatenation function. """


def concat(str1: str, str2: str) -> str:
    """
    Description:
        Concatenate two strings.

    Args:
        - str1 (str): first string to concatenate.
        - str2 (str): second string to concatenate.

    Returns:
        (str): the concatenated string of the two input strings.
    """
    return (str1 + str2)
