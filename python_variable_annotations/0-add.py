#!/usr/bin/env python3
""" Module with a float addition function. """


def add(a: float, b: float) -> float:
    """
    Description:
        Add two floating-point numbers.

    Args:
        - a (float): first input.
        - b (float): second input.

    Returns:
        (float): the sum of the two input numbers.
    """
    return (a + b)
